# string based functions :
company = "It VEDANT, Mumbai"
print("length : ", len(company))
print("Capitalize : ", company.capitalize())
print("Lower : ", company.lower())
print("Title : ", company.title())
print("Upper : ", company.upper())

s1 = "kalyani"
print("s1 ", s1, " isalpha() : ", s1.isalpha())
s2 = "sankpalkalyani93gmailcom"
print("s2 ", s2, " isalnum() : ", s2.isalnum())
s3 = "422003"
print("s3 ", s3, " isdigit() : ", s3.isdigit())
s4 = "nashik"
print("s4 ", s4, " islower() : ", s4.islower())
s5 = "INDIA"
print("s5 ", s5, " isupper() : ", s5.isupper())