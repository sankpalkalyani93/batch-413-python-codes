#Arithmetic operations

num1 = 100
num2 = 10

result = num1 + num2
print("Summation : ", result)

print("Subtraction : ", num1, " - ", num2, " = ", (num1-num2))

result = num1 * num2
print("Multiplication : ", result)

result = num1 / num2   
print("Division : ", result)

result = num1 // num2   # floor division: removing the decimal values
print("Division with floor type : ", result)

result = num1 % num2
print("Modulus : ", result)