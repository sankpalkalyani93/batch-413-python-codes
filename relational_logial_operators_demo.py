# Relational operators
x = 5
y = 7

print(x, " > ", y, (x>y))
print(x, " >= ", y, (x>=y))
print(x, " < ", y, (x<y))
print(x, " <= ", y, (x<=y))
print(x, " == ", y, (x==y))
print(x, " != ", y, (x!=y))

# Logical Operators
z = 4
print(x, " > ", y, " and ", x, " > ", z, (x>y and x>z))
print(x, " > ", y, " or ", x, " > ", z, (x>y or x>z))